        /// 實心線
        /// </summary>
        _1111111111111111,

        /// <summary>
        /// 虛線樣式之一，1 表示線段，0 表示空心
        /// </summary>
        _1111000000000000,

        /// <summary>
        /// 虛線樣式之一，1 表示線段，0 表示空心
        /// </summary>
        _1111000011110000,

        /// <summary>
        /// 虛線樣式之一，1 表示線段，0 表示空心
        /// </summary>
        _1100110011001100,

        /// <summary>
        /// 虛線樣式之一，1 表示線段，0 表示空心
        /// </summary>
        _1111111011111110,
    }
}